// 获取动物对话文案
export const getAnimalDialogue = (animalType, state, emotion = null, tradeType = null) => {
  const animalData = animalDialogue[animalType] || animalDialogue.default;
  
  // 如果有交易类型，优先返回交易相关对话
  if (tradeType && animalData.trade && animalData.trade[tradeType]) {
    const tradeDialogues = animalData.trade[tradeType];
    return tradeDialogues[Math.floor(Math.random() * tradeDialogues.length)];
  }
  
  // 如果有情绪状态，返回情绪相关对话
  if (emotion && animalData.emotions && animalData.emotions[emotion]) {
    const emotionDialogues = animalData.emotions[emotion];
    return emotionDialogues[Math.floor(Math.random() * emotionDialogues.length)];
  }
  
  // 根据动物状态返回相应对话
  if (state && animalData[state]) {
    const stateDialogues = animalData[state];
    return stateDialogues[Math.floor(Math.random() * stateDialogues.length)];
  }
  
  // 默认返回日常对话
  if (animalData.daily) {
    const dailyDialogues = animalData.daily;
    return dailyDialogues[Math.floor(Math.random() * dailyDialogues.length)];
  }
  
  return "...";
};
